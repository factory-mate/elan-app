import { UnitAPI } from './api'
import type { UnitAddDto, UnitEditDto } from './types'

export const useStartMutation = () => {
  const { showMessage } = useMessage()
  return useMutation({
    mutationFn: (ids: string[]) => UnitAPI.start(ids),
    onSuccess: () => showMessage('success')
  })
}

